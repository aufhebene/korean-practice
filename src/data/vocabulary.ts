// 카테고리별 단어 가져오기
export function getVocabularyByCategory(categoryId: string): Vocabulary[] {
  return vocabularyData.filter((v) => v.category === categoryId);
}

// 레벨별 단어 가져오기
export function getVocabularyByLevel(level: number): Vocabulary[] {
  return vocabularyData.filter((v) => v.level === level);
}

// 랜덤 단어 가져오기
export function getRandomVocabulary(count: number): Vocabulary[] {
  const shuffled = [...vocabularyData].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}

// 퀴즈용 옵션 생성 (정답 + 오답 3개)
export function generateQuizOptions(correctWord: Vocabulary): string[] {
  const wrongOptions = vocabularyData
    .filter((v) => v.id !== correctWord.id && v.category === correctWord.category)
    .sort(() => Math.random() - 0.5)
    .slice(0, 3)
    .map((v) => v.meanings.en);

  const options = [correctWord.meanings.en, ...wrongOptions];
  return options.sort(() => Math.random() - 0.5);
}
